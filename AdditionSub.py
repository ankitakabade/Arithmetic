# Program: Addition and Subtraction of Two Numbers

# Input two numbers
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Perform addition
addition = num1 + num2

# Perform subtraction
subtraction = num1 - num2

# Display results
print("\nResults:")
print("Addition:", addition)
print("Subtraction:", subtraction)